#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	int	number;

	number = 48;
	while (number <= 57)
	{
		ft_putchar(number);
		number++;
	}
}